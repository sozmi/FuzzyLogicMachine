// Типы токенов
enum TokenType
{
    Identificator,   // для названий и терминов в квадратных скобках
    Has,         // связующий глагол "есть"
    No, // унарный оператор "НЕ"
    And, // бинарный оператор "И"
    Or,// бинарный оператор "ИЛИ"
    Modifier,     // модификаторы
    Left_Bracket, // открытая скобка
    Right_Bracket, // закрытая скобка
    EOF   // маркер конца ввода
}
