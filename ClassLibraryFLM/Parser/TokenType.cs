// <ун. оператор> := 'НЕ'
// <бин. оператор> := 'И' | 'ИЛИ'
// <модификатор> := ОЧЕНЬ | БОЛЕЕ ИЛИ МЕНЕЕ | МНОГО БОЛЬШЕ

// <высказывание> :=  <наименование> 'есть' <терм> | <наименование> 'есть' <модификатор> <терм>
// <наименование> := '[' 'строка' ']' 
